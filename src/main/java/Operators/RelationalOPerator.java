package Operators;

public class RelationalOPerator {
//<,>,<=,>=,==
	public static void main(String[] args) {
		int a=25;
		int b=30;
		System.out.println(a>b);
		System.out.println(a<b);
		System.out.println(a>=b);
		System.out.println(a<=b);
		System.out.println(a==b);
		System.out.println(a!=b);
	}
}
