package basicjava.SelfIntro;

public class ArithmeticOperator {

	public static void main(String[] args) {
		int a=10;
		int b=20;
		int c=30;
		System.out.println(a+b+c);
		System.out.println(c-b);
		System.out.println(a*b);
		System.out.println((float)b/a);
		System.out.println((float)b%a);

	}

}
